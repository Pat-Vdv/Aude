   <div id="intro">
      <p>
         Aude is a free software for manipulating, learning, and teaching finite state automata and the automata theory.
      </p>

      <p class="illustr">
         <img alt="" src="aude/doc/word-execution.png" />
      </p>

      <p>
        You can use Aude online: <a href="aude/">http://aude.forge.imag.fr/aude/</a>.
      </p>

      <p>
        You can also <a href="?p=use#offline">download it</a>. Beware, <a href="?p=use#unsupported">Aude is unlikely to work in Internet Explorer or Edge</a>. The recommended browser is Firefox. Chromium (Chrome) is also supported.
      </p>

      <h2> Features </h2>
      <ul id="feature-list">
         <li> <strong>Draw or code automata</strong>.</li>
         <li> <strong>Execute words</strong>.</li>
         <li> Apply <strong>basic algorithms</strong> on automata like <strong>determinization</strong>, <strong>minimization</strong>, <strong>completion</strong>, <strong>complementation</strong>, <strong>epsilon-removal</strong> and more.</li>
         <li> <strong>Write your own algorithms</strong> in a language <strong>specially crafted</strong> for manipulating sets and automata.</li>
         <li> Perform or write <strong>Quizzes</strong>.</li>
      </ul>

<h2> Browser support </h2>

<h3 id="unsupported"> Supported browsers </h3>

<p>
   <strong> Firefox (Recommended).</strong> The latest version of Firefox is the
   recommended browser to use Aude.
</p>

<p>
    <strong> Google Chrome, Opera. </strong> Aude is also actively tested in
    Chromium (Google Chrome). Latest versions of Opera are based on Chromium so
    Aude should equally work in Opera.
</p>

<p>
    <strong> Safari &amp; other Webkit-based browsers. </strong>
    It is likely that Safari will also work. Other recent Webkit-powered
    browsers should also work (Aude have been seen working in Rekonq, Arora and
    Epiphany, Gnome's default browser).
</p>

<h3 id="unsupported"> Unsupported browsers </h3>

<p>
    Support in other browsers is uncertain. Aude is not big, however it is not a
    trivial program neither and testing in any particular browser is important
    to fix or work around specific bugs and differences.
</p>

<p>
    <strong>IE / Edge.</strong>  Aude should not work on Internet Explorer and
    Edge. Nobody among the active Aude team (mainly composed by me) is eager to
    launch a full Windows system, run IE and risk to enter a long painful debug
    session to make this happen. There are so many better browsers out there and
    so many better things to do. So if Aude doesn't work for you, please do
    yourself a favor and download
    <a href="https://www.mozilla.org/fr/firefox/new/"> Mozilla Firefox </a> or
    any other browser mentioned above.
<p>

<p>
    Patches to add support in any browser (including IE and Edge) are welcome.
    Be aware, however, that as Aude evolves, any previous support might break
    if nobody is actively in charge of this support.
</p>

<h4 id="unsupported"> I am using links, w3m, netsurf, curl (wget) + cat or telnet to browse the Web.</h4>
<p>
    These are amazing browsers in which Aude will probably never run. Sorry.
</p>
